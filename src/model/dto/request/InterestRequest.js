class InterestRequest {
    constructor(rate, creditConfiguration, payInterest, typeInterest, capitalizationPeriod) {
      this.rate = rate;
      this.creditConfiguration = creditConfiguration;
      this.payInterest = payInterest;
      this.typeInterest = typeInterest;
      this.capitalizationPeriod = capitalizationPeriod;
    }
}

export default InterestRequest;
 